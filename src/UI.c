bool UI_SetActiveDevice(enum activeDevice device)
{
    if(curActiveDevice != UI_DEVICE_NONE)
        return false;
    else
        curActiveDevice = device;
    return true;
}

void UI_SPI_Callback(uintptr_t callback)
{
    switch(curActiveDevice)
    {
        case UI_DEVICE_KEYPAD:
            Keypad_SPICallback((uintptr_t) NULL);
            break;
        case UI_DEVICE_OLED:
            OLED_SPICallback((uintptr_t) NULL);
            break;
        default:
            break;
    }
    curActiveDevice = UI_DEVICE_NONE;
}

